package aula_08_q1;

public class Animal {
	protected String nome;
	protected double peso;
	
	protected String getNome() {
		return this.nome;
	}
	protected double getPeso() {
		return this.peso;
	}
	protected void setNome(String nome) {
		this.nome = nome;
	}
	protected void setPeso(double peso) {
		this.peso = peso;
	}

}
