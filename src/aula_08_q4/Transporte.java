package aula_08_q4;

public class Transporte {
	
	protected double velocidade;
	protected int numeroDePassageiro;
	protected int numeroDeRodas;
	protected String cor;
	
	
	
	protected String getCor() {
		return cor;
	}
	protected void setCor(String cor) {
		this.cor = cor;
	}
	protected double getVelocidade() {
		return velocidade;
	}
	protected void setVelocidade(double velocidade) {
		this.velocidade = velocidade;
	}
	protected int getNumeroDePassageiro() {
		return numeroDePassageiro;
	}
	protected void setNumeroDePassageiro(int numeroDePassageiro) {
		this.numeroDePassageiro = numeroDePassageiro;
	}
	protected int getNumeroDeRodas() {
		return numeroDeRodas;
	}
	protected void setNumeroDeRodas(int numeroDeRodas) {
		this.numeroDeRodas = numeroDeRodas;
	}
	
	

}
